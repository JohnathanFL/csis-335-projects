public class Customer {
  String fName, lName, streetAddr, city, state, zip, cell, email;

  public Customer(String fName, String lName, String streetAddr, String city, String state, String zip, String cell, String email) {
    this.fName = fName;
    this.lName = lName;
    this.streetAddr = streetAddr;
    this.city = city;
    this.state = state;
    this.zip = zip;
    this.cell = cell;
    this.email = email;
  }
}
